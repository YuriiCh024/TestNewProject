# Задача 4: Таблиця множення
# Напиши програму, яка запитує у користувача число (наприклад, 7)
# і за допомогою циклу виводить для нього таблицю множення від 1 до 10
# у форматі: 7 x 1 = 7 7 x 2 = 14 ... і так далі.

num = int(input("Enter a number: "))
for i in range(1, 11):
    print(f"{num} x {i} = {i * num}")